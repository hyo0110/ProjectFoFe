package com.sns.dto;

public class FollowerTalkDTO {
	
	private String followerTalkIdx;
	private String id;
	private String followerTalkUser_name;
	private String followerTalkContent;
	
	public String getFollowerTalkIdx() {
		return followerTalkIdx;
	}
	public void setFollowerTalkIdx(String followerTalkIdx) {
		this.followerTalkIdx = followerTalkIdx;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getFollowerTalkUser_name() {
		return followerTalkUser_name;
	}
	public void setFollowerTalkUser_name(String followerTalkUser_name) {
		this.followerTalkUser_name = followerTalkUser_name;
	}
	public String getFollowerTalkContent() {
		return followerTalkContent;
	}
	public void setFollowerTalkContent(String followerTalkContent) {
		this.followerTalkContent = followerTalkContent;
	}

}
