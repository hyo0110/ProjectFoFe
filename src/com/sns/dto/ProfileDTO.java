package com.sns.dto;

public class ProfileDTO {

	private String id;
	private String nickname;
	private String myBirth;
	private String blood;
	private String addr;
	private String major;
	private String seduWay;
	private String motto;
	private String fMovie;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getMyBirth() {
		return myBirth;
	}
	public void setMyBirth(String myBirth) {
		this.myBirth = myBirth;
	}
	public String getBlood() {
		return blood;
	}
	public void setBlood(String blood) {
		this.blood = blood;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getMajor() {
		return major;
	}
	public void setMajor(String major) {
		this.major = major;
	}
	public String getSeduWay() {
		return seduWay;
	}
	public void setSeduWay(String seduWay) {
		this.seduWay = seduWay;
	}
	public String getMotto() {
		return motto;
	}
	public void setMotto(String motto) {
		this.motto = motto;
	}
	public String getfMovie() {
		return fMovie;
	}
	public void setfMovie(String fMovie) {
		this.fMovie = fMovie;
	}
	
}
