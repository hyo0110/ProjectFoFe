package com.sns.dto;

public class GuestBookDTO {
	
	private String guestBookIdx;
	private String id;
	private String guestBookUser_name;
	private String guestBookContent;
	private String guestBookReg_date;
	
	public String getGuestBookIdx() {
		return guestBookIdx;
	}
	public void setGuestBookIdx(String guestBookIdx) {
		this.guestBookIdx = guestBookIdx;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getGuestBookUser_name() {
		return guestBookUser_name;
	}
	public void setGuestBookUser_name(String guestBookUser_name) {
		this.guestBookUser_name = guestBookUser_name;
	}
	public String getGuestBookContent() {
		return guestBookContent;
	}
	public void setGuestBookContent(String guestBookContent) {
		this.guestBookContent = guestBookContent;
	}
	public String getGuestBookReg_date() {
		return guestBookReg_date;
	}
	public void setGuestBookReg_date(String guestBookReg_date) {
		this.guestBookReg_date = guestBookReg_date;
	}
	

}
